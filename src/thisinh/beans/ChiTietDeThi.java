package thisinh.beans;

public class ChiTietDeThi {

	private String TenDe;
	private String ThoiGianThi;
	private String MaCauHoi;
	private String NoiDung;
	private String CauA;
	private String CauB;
	private String CauC;
	private String CauD;
	private String DapAn;
	
	public ChiTietDeThi( String tenDe, String thoiGianThi, String maCauHoi, String noiDung, String cauA,
			String cauB, String cauC, String cauD, String dapAn) {
		super();

		TenDe = tenDe;
		ThoiGianThi = thoiGianThi;
		MaCauHoi = maCauHoi;
		NoiDung = noiDung;
		CauA = cauA;
		CauB = cauB;
		CauC = cauC;
		CauD = cauD;
		DapAn = dapAn;
	}
	
	public String getTenDe() {
		return TenDe;
	}
	/**
	 * @param tenDe the tenDe to set
	 */
	public void setTenDe(String tenDe) {
		TenDe = tenDe;
	}
	/**
	 * @return the thoiGianThi
	 */
	public String getThoiGianThi() {
		return ThoiGianThi;
	}
	/**
	 * @param thoiGianThi the thoiGianThi to set
	 */
	public void setThoiGianThi(String thoiGianThi) {
		ThoiGianThi = thoiGianThi;
	}
	/**
	 * @return the maCauHoi
	 */
	public String getMaCauHoi() {
		return MaCauHoi;
	}
	/**
	 * @param maCauHoi the maCauHoi to set
	 */
	public void setMaCauHoi(String maCauHoi) {
		MaCauHoi = maCauHoi;
	}
	/**
	 * @return the noiDung
	 */
	public String getNoiDung() {
		return NoiDung;
	}
	/**
	 * @param noiDung the noiDung to set
	 */
	public void setNoiDung(String noiDung) {
		NoiDung = noiDung;
	}
	/**
	 * @return the cauA
	 */
	public String getCauA() {
		return CauA;
	}
	/**
	 * @param cauA the cauA to set
	 */
	public void setCauA(String cauA) {
		CauA = cauA;
	}
	/**
	 * @return the cauB
	 */
	public String getCauB() {
		return CauB;
	}
	/**
	 * @param cauB the cauB to set
	 */
	public void setCauB(String cauB) {
		CauB = cauB;
	}
	/**
	 * @return the cauC
	 */
	public String getCauC() {
		return CauC;
	}
	/**
	 * @param cauC the cauC to set
	 */
	public void setCauC(String cauC) {
		CauC = cauC;
	}
	/**
	 * @return the cauD
	 */
	public String getCauD() {
		return CauD;
	}
	/**
	 * @param cauD the cauD to set
	 */
	public void setCauD(String cauD) {
		CauD = cauD;
	}
	/**
	 * @return the dapAn
	 */
	public String getDapAn() {
		return DapAn;
	}
	/**
	 * @param dapAn the dapAn to set
	 */
	public void setDapAn(String dapAn) {
		DapAn = dapAn;
	}

	
	
}
